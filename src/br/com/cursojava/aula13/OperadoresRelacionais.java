package br.com.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("Valor1 é igual ao valor2: " + (valor1 == valor2));
		System.out.println("Valor1 é diferente ao valor2: " + (valor1 != valor2));
		System.out.println("Valor1 é maior que o valor2: " + (valor1 > valor2));
		System.out.println("Valor1 é maior ou igual ao valor2: " + (valor1 >= valor2));
		System.out.println("Valor1 é menor que o valor2: " + (valor1 < valor2));
		System.out.println("Valor1 é menor ou igual ao valor2: " + (valor1 <= valor2));
	}

}
